package com.tictactoe.tictactoe.domain.utils;

public class CellPos {
    private int row;
    private int column;

    public CellPos(int row, int column) {
        this.row = row;
        this.column = column;
    }

    public int getRow() {
        return row;
    }

    public void setRow(int row) {
        this.row = row;
    }

    public int getColumn() {
        return column;
    }

    public void setColumn(int column) {
        this.column = column;
    }
}
